package org.lanqiao.entity;

public class Department {
    // 部门编号
    private int dNo;
    // 部门名称
    private String dName;


    public int getDNo() {
        return this.dNo;
    }

    public void setDNo(int dNo) {
        this.dNo = dNo;
    }

    public String getDName() {
        return this.dName;
    }

    public void setDName(String dName) {
        this.dName = dName;
    }
}